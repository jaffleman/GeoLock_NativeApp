BEGIN;
DROP TABLE IF EXISTS marker cascade;
DROP TABLE IF EXISTS acces cascade;


CREATE TABLE marker (
id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
longitude INTEGER NOT NULL,
latitude INTEGER NOT NULL);

CREATE TABLE acces (
id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
type TEXT NULL,
code TEXT NOT NULL,
mk INTEGER NOT NULL);

ALTER TABLE acces ADD CONSTRAINT acces_mk_marker_id FOREIGN KEY (mk) REFERENCES marker(id);
